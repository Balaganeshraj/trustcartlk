// AI function to detect category based on product name
export const detectProductCategory = (productName: string): string => {
  const name = productName.toLowerCase();
  
  // Quick local mapping for common items (immediate response)
  const quickMap: { [key: string]: string } = {
    'banana': 'Food & Beverages',
    'apple': 'Food & Beverages', 
    'orange': 'Food & Beverages',
    'rice': 'Food & Beverages',
    'bread': 'Food & Beverages',
    'milk': 'Food & Beverages',
    'pen': 'Office Supplies',
    'pencil': 'Office Supplies',
    'paper': 'Office Supplies',
    'notebook': 'Office Supplies',
    'shirt': 'Fashion',
    'dress': 'Fashion',
    'shoes': 'Shoes & Footwear',
    'phone': 'Mobile Phones & Accessories',
    'laptop': 'Computers & Laptops'
  };
  
  // Check quick map first for instant results
  for (const [key, category] of Object.entries(quickMap)) {
    if (name.includes(key)) {
      return category;
    }
  }
  
  // Score each category based on keyword matches
  const categoryScores: { [key: string]: number } = {};
  
  Object.entries(categoryKeywords).forEach(([category, keywords]) => {
    let score = 0;
    keywords.forEach(keyword => {
      if (name.includes(keyword.toLowerCase())) {
        // Give higher score for exact matches and longer keywords
        score += keyword.length * 2;
        if (name === keyword.toLowerCase()) {
          score += 10; // Bonus for exact match
        }
      }
    });
    if (score > 0) {
      categoryScores[category] = score;
    }
  });
  
  // Find the category with the highest score
  if (Object.keys(categoryScores).length > 0) {
    const bestCategory = Object.entries(categoryScores)
      .sort(([,a], [,b]) => b - a)[0][0];
    return bestCategory;
  }
  
  // Fallback: try to match with category names directly
  for (const category of ecommerceCategories) {
    if (name.includes(category.toLowerCase())) {
      return category;
    }
  }
  
  return 'Uncategorized';
};

// Get category suggestions based on partial input
export const getCategorySuggestions = (input: string, limit: number = 5): string[] => {
  const searchTerm = input.toLowerCase();
  
  if (!searchTerm) return ecommerceCategories.slice(0, limit);
  
  const suggestions = ecommerceCategories.filter(category =>
    category.toLowerCase().includes(searchTerm)
  );
  
  return suggestions.slice(0, limit);
};

